/* 
 * twosComp2SignMag - Convert from two's complement to sign-magnitude 
 *   where the MSB is the sign bit
 *   You can assume that x > TMin
 *   Example: twosComp2SignMag(-5) = 0x80000005.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 4
 */
int twosComp2SignMag(int x) 
{
    // To convert from two's complement to signed magnitude, first subtract 1, then invert
    // all the bits, and set the left-most bit to be 1 (this is for negative numbers). For 
    // positive numbers, keep as is.
 
    int positive = !(x >> 31); // 1 for nonnegative x, 0 otherwise
    int neg = ~(x + (~1 + 1)); // Subtract 1, then invert all the bits
    int mask = (positive << 31) >> 31; // Filters out based on sign of x
    neg = neg | (1 << 31); // Set the first bit if negative
    return (mask & x) | (~mask & neg);
}
/*
 * bitParity - returns 1 if x contains an odd number of 0's
 *   Examples: bitParity(5) = 0, bitParity(7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int bitParity(int x) 
{
    // The key here is to recursively arrive at a juncture such that we can XOR all the bits.
    // If we XOR all the bits, an odd number of 0's will result in 1, and 0 elsewise. Do this 
    // by repeatedly splitting the number in half and XORing it with the shifted version of itself.

    x = x ^ (x >> 16);
    x = x ^ (x >> 8);
    x = x ^ (x >> 4);
    x = x ^ (x >> 2);
    x = x ^ (x >> 1);
    return x & 1; // If odd, the last bit will be a 1
}
