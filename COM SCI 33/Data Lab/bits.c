/* 
 * isEqual - return 1 if x == y, and 0 otherwise 
 *   Examples: isEqual(5,5) = 1, isEqual(4,5) = 0
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int isEqual(int x, int y) 
{
    // If the numbers are equal, all the bits match, meaning XORing will result in all zeros. 
    // Performing a logical not will result in outputting the correct number.

    return !(x ^ y);
}
/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) 
{
    // Determine the sign of the parameter, and then using bitwise operators simulate an if-else
    // statement returning the result if x is positive or else the result if x is negative.

    int positive = !(x >> 31); // Stores 1 for nonnegative numbers and 0 otherwise
    int nMinusOne = (~n + (n << 1)); // n - 1
    int negResult = !(~x >> nMinusOne);
    int posResult = !(x >> nMinusOne);
    return (!positive & negResult) | (positive & posResult);
}
