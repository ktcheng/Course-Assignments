/* 
 * isLess - if x < y  then return 1, else return 0 
 *   Example: isLess(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLess(int x, int y) 
{
    // For most cases, checking if x - y < 0 is sufficient. For edge/overflow cases, check
    // for differing signs, such that we can perform sign comparison. 

    int xPositive = !(x >> 31); // Stores 1 for nonnegative x
    int yPositive = !(y >> 31); // Stores 1 for nonnegative y
    int negY = ~y + 1; // -y
    int res = x + negY;
    return (!(xPositive ^ yPositive) & !!(res >> 31)) | ((xPositive ^ yPositive) & yPositive);
}
/*
 * multFiveEighths - multiplies by 5/8 rounding toward 0.
 *   Should exactly duplicate effect of C expression (x*5/8),
 *   including overflow behavior.
 *   Examples: multFiveEighths(77) = 48
 *             multFiveEighths(-22) = -13
 *             multFiveEighths(1073741824) = 13421728 (overflow)
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 3
 */
int multFiveEighths(int x) 
{
    // The trick here is to split the number such that x = x1 + x2, where x1 is the largest
    // whole number perfectly divisible by 8, and x2 is the remainder when dividing by 8.
    // For negative instances of rounding towards zero, selectively add 1 when appropriate.

    int whole = x >> 3;
    int remainder = x & 7; // Remainder of x / 8
    int c = ((remainder << 2) + remainder + (x >> 31 & 7)) >> 3; // Selectively add 1
    return (whole << 2) + whole + c;
}
